//--------------------------------------------------------------------------------
const std = @import("std");
const unittest = @import("libs/unittest.zig");
var lib = @import("lib-template.zig").init(.{});
//--------------------------------------------------------------------------------
pub fn main(init: std.process.Init) !void {
    //----------------------------------------------------------------------------
    var ut = try unittest.init(.{ .io = init.io });
    //----------------------------------------------------------------------------
    {
        //----------------------------------------
        const name = "lib.init";
        //----------------------------------------
        try ut.compareByte(name, 0, lib.example_byte);
        //----------------------------------------
    }
    //----------------------------------------------------------------------------
    {
        //----------------------------------------
        const name = "lib.setByte";
        //----------------------------------------
        const result = lib.setByte(1);
        //----------------------------------------
        try ut.compareBool(name, true, result);
        //----------------------------------------
    }
    //----------------------------------------------------------------------------
    {
        //----------------------------------------
        const name = "lib.getByte";
        //----------------------------------------
        const result = lib.getByte();
        //----------------------------------------
        try ut.compareByte(name, 1, result);
        //----------------------------------------
    }
    //----------------------------------------------------------------------------
    try ut.printSummary();
    //----------------------------------------------------------------------------
}
