//--------------------------------------------------------------------------------
const std = @import("std");
//--------------------------------------------------------------------------------
pub fn byteToString(byte: u8) [1]u8 {
    //----------------------------------------------------------------------------
    return [1]u8{byte};
    //----------------------------------------------------------------------------
}
//--------------------------------------------------------------------------------
pub fn main() !void {
    //----------------------------------------------------------------------------
    const result1 = byteToString(0x42);
    std.debug.print("byteToString: {s}\n", .{result1});
    //----------------------------------------
    const data = "ABCDE";
    const byte: u8 = 0x42;
    //----------------------------------------
    const result2 = std.mem.indexOf(u8, data, &[_]u8{byte});
    std.debug.print("indexOf: {any}\n", .{result2});
    //----------------------------------------------------------------------------
}
