//------------------------------------------------------------

const std = @import("std");

//------------------------------------------------------------

pub fn main(init: std.process.Init) !void {
    //------------------------------------------------------------
    const max = 10;
    //------------------------------------------------------------
    for (0..max) |i| {
        //----------------------------------------
        var byte: [1]u8 = undefined;
        init.io.random(&byte);
        //----------------------------------------
        std.debug.print("index {d}, random u8 = 0x{X:0>2}\n", .{ i, byte[0] });
        //----------------------------------------
    }
    //------------------------------------------------------------
}

