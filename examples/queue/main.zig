//------------------------------------------------------------
const std = @import("std");
//------------------------------------------------------------
const Signal = enum(u8) {
    ping1,
    ping2,
    shutdown,
};
const SignalQueue = struct {
    io: std.Io,
    queue: std.Io.Queue(Signal),
    countdown: usize,
};
//------------------------------------------------------------
pub fn main(init: std.process.Init) !void {
    //------------------------------------------------------------
    var queue_buffer: [1]Signal = undefined;
    //------------------------------------------------------------
    var sq = SignalQueue{
        .io = init.io,
        .queue = std.Io.Queue(Signal).init(&queue_buffer),
        .countdown = 5,
    };
    //------------------------------------------------------------
    const send_thread1 = try std.Thread.spawn(.{}, sendPing1OnlySignals, .{&sq});
    send_thread1.detach();
    const send_thread2 = try std.Thread.spawn(.{}, sendPing2AndShutdownSignals, .{&sq});
    send_thread2.detach();
    //------------------------------------------------------------
    std.debug.print("waiting for signals...\n", .{});
    //------------------------------------------------------------
    var count: usize = 0;
    const max_count: usize = 100;
    //------------------------------------------------------------
    while (count < max_count) : (count += 1) {
        //-----------------------------------
        if (try checkSignals(&sq)) {
            std.debug.print("shutting down\n", .{});
            break;
        }
        //-----------------------------------
    }
    //------------------------------------------------------------
}
//------------------------------------------------------------
fn checkSignals(sq: *SignalQueue) !bool {
    //------------------------------------------------------------
    const signal = try sq.queue.getOne(sq.io);
    //-----------------------------------
    std.debug.print("({} signal)\n", .{signal});
    //-----------------------------------
    return signal == .shutdown;
    //------------------------------------------------------------
}
//------------------------------------------------------------
fn sendPing1OnlySignals(sq: *SignalQueue) !void {
    //------------------------------------------------------------
    while (sq.countdown > 0) {
        //-----------------------------------
        _ = try sq.queue.putOne(sq.io, .ping1);
        //-----------------------------------
        try sq.io.sleep(.fromMilliseconds(3), .real);
        //-----------------------------------
    }
    //------------------------------------------------------------
    return;
    //------------------------------------------------------------
}
//------------------------------------------------------------
fn sendPing2AndShutdownSignals(sq: *SignalQueue) !void {
    //------------------------------------------------------------
    while (sq.countdown > 0) {
        //-----------------------------------
        sq.countdown -= 1;
        //-----------------------------------
        _ = try sq.queue.putOne(sq.io, .ping2);
        //-----------------------------------
        try sq.io.sleep(.fromMilliseconds(5), .real);
        //-----------------------------------
    } else {
        //-----------------------------------
        _ = try sq.queue.putOne(sq.io, .shutdown);
        //-----------------------------------
    }
    //------------------------------------------------------------
    return;
    //------------------------------------------------------------
}
